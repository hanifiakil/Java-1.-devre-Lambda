public class C07_PreDecrement_PostDecrement {
    public static void main(String[] args) {

        int sayı=12;
        sayı--;
        System.out.println(sayı);
        int a=sayı--;
        System.out.println(a);
        System.out.println(sayı);
        System.out.println(--a);
    }
}
